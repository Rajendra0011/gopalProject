package Array_star_program;

 
public class NUmberArray_program {

	public static void main(String[] args) 
	{
      int k=0;
      for(int i=1;i<5;i++)
      {
    	  for(int j=1;i<=i;j++)
    	  {
    		  System.out.print(k);
    		  k++;
    	  }
    	  System.out.println(" ");
      }
      
		

	}

}
